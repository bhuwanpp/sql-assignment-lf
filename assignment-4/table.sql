
CREATE TABLE employees (
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    sex CHAR(1),
    doj DATE,
    current_date_ DATE,
    designation VARCHAR(50),
    age INTEGER,
    salary NUMERIC(10, 2),
    unit VARCHAR(50),
    leaves_used INT,
    leaves_remaining INT,
    ratings INT,
    past_exp INT
);
